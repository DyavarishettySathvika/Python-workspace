#->Slicing String
x = "Sathvika Dyavarishetty"
print(x[5:19])
# In Slicing string we need to determine by starting string number to ending of string number.This counting will start from 1 to so on
y = "Dell"
print(y[1:3])
y="Slicing String"
print(y[3:8])


#->Slice from the Start
a = "Sathvika"
print(a[:5])
# It will start from starting letter. It will count spacing of a string also.It will print starting values
b="Vinit Nandhan"
print(b[:6])

#->Slice from the End
a="St.Martins"
print(a[4:])
# . This symbol will also count a value.It will print ending values.
b='Engineering'
print(b[5:])

#->Negative Indexing
b = "Awesome!"
print(b[-5:-2])
