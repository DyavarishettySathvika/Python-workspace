#->Evaluating Values and Variables
print(bool("Broh"))
print(bool(14))

#Giving X value and Y value
x = "Broh"
y = 14
print(bool(x))
print(bool(y))

#Different symbols were used here
print(bool(""))      # Empty string 
print(bool("Hi"))    # Non-empty string 
print(bool(0))       # Zero 
print(bool(100))     # Non-zero 

#By using list
print(bool([]))      
print(bool([1, 2]))  
