print(len(thislist))
my_list = [1, 2, 3, 4, 5]
print(len(my_list))  
fruits = ["apple", "banana", "cherry", "date"]
print(len(fruits))  
mixed = [1, "hello", True, [1, 2]]
print(len(mixed)) 

