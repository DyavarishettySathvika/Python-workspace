#List Items
#List items are ordered, changeable, and allow duplicate values.List items are indexed, the first item has index [0], the second item has index [1]
#Ordered List
# Creating an ordered list
fruits = ["apple", "banana", "cherry"]
print(fruits[0])  
print(fruits[1])  
my_list = [1, "hello", 3.14, True]
print(my_list)
school = ["books","pens","bag"]
print(school[1])
print(school[2])

#Changeable list
# Create a list
numbers = [10, 20, 30]
print("Original list:", numbers)# Change an element
numbers[1] = 99
print("After change:", numbers)# Add a new element at the end
numbers.append(40)
print("After append:", numbers)

#Allow Duplicates
thislist = ["apple", "banana", "cherry", "apple", "cherry"]
print(thislist)
my_list = [1, 2, 2, 3, 3, 3]
print(my_list)

