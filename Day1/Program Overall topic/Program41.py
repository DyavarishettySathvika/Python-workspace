num = int(input("Enter a number: "))

print("You entered:", num)

if num % 2 == 0:
    print("Checking condition")
    print("Number is divisible by 2")
    print("So it is Even")
else:
    print("Checking condition")
    print("Number is not divisible by 2")
    print("So it is Odd")

print("Program Successful")