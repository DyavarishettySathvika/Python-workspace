#Range of Negative Indexes(Specify negative indexes if you want to start the search from the end of the tuple)
fruits=('Mango','Pineapple','Apple','Orange','Kiwi','Cherry')
print(fruits[-5:-2])
print(fruits[-4:-1])
print(fruits[-4:1])
print(fruits[4:-1])
print(fruits[-2:5])

